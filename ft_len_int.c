#include "libft.h"

int	ft_len_int(int n)
{
	int				len;
	unsigned int	nn;

	nn = n;
	len = 0;
	if (n == 0)
		return (1);
	if (n < 0)
	{
		nn = -n;
		len++;
	}
	while (nn > 0)
	{
		if (nn > 0)
			len++;
		nn = nn / 10;
	}
	return (len);
}
