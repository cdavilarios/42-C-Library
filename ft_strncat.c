#include "libft.h"

char	*ft_strncat(char *s1, char const *s2, size_t n)
{
	int x;
	int y;

	x = (int)ft_strlen(s1);
	y = (int)n;
	while (y > 0 && *s2)
	{
		s1[x] = *s2++;
		x++;
		y--;
	}
	s1[x] = '\0';
	return (s1);
}
